public class Pattern4 
{

	public static void main(String[] args)
	{
		int i,k;
		i=1;
		k=1;
	
		while(i <=5)
		{
			for(int j = k;j <=5;j++)
			{
				System.out.print(j);
			}
			System.out.println();	
		i++;
		k++;
		}
	}

}
